package com.lyzdroid.sharewind.gson;

public class Now {

    // 实况观测时间
    public String obsTime;

    // 实况温度，默认单位：摄氏度
    public String temp;

    // 实况体感温度，默认单位：摄氏度
    public String feelsLike;

    // 当前天气状况和图标的代码
    public String icon;

    // 实况天气状况的文字描述，包括阴晴雨雪等天气状态的描述
    public String text;

    // 实况风向360角度
    public String wind360;

    // 实况风向
    public String windDir;

    // 实况风力等级
    public String windScale;

    // 实况风速，公里/小时
    public String windSpeed;

    // 实况相对湿度，百分比数值
    public String humidity;

    // 实况降水量，默认单位：毫米
    public String precip;

    // 实况大气压强，默认单位：百帕
    public String pressure;

    // 实况能见度，默认单位：公里
    public String vis;

    // 实况云量，百分比数值
    public String cloud;

    // 实况露点温度
    public String dew;

}
